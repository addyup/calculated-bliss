package com.mandeer.websc.model;

public class BuildDetails {
    private String name;
    private String buildType;
    private String multiLevelDetail;
    private String phoneNumber;
    private String email;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getBuildType() {
		return buildType;
	}
	public void setBuildType(String buildType) {
		this.buildType = buildType;
	}
	public String getMultiLevelDetail() {
		return multiLevelDetail;
	}
	public void setMultiLevelDetail(String multiLevelDetail) {
		this.multiLevelDetail = multiLevelDetail;
	}
	public String getPhoneNumber() {
		return phoneNumber;
	}
	public void setPhoneNumber(String phoneNumber) {
		this.phoneNumber = phoneNumber;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}

    // Getters and Setters
    
}
